#include <stdio.h>

/**
 * add - add two integers
 * @a: first int
 * @b: second int
 * return: addition
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - sub two integers
 * @a: first int
 * @b: second int
 * return: subraction final result
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mod - mod of two integers
 * @a: first int
 * @b: second int
 * return: final answer
 */

int mod(int a, int b)
{
	return (a % b);
}

/**
 * mul - multiply two integers
 * @a: first int
 * @b: second int
 * return: multiply
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div -divide two integers
 * @a: first int
 * @b: second int
 * return: division result
 */

int div(int a, int b)
{
	return (a / b);
}
